// Define Variables: Create a Dart program that defines and initializes variables of the following types: int, double, String, bool, and List.


void main() {
  // Defining and initializing an integer variable
  int age = 29;

  // Defining and initializing a double variable
  double height = 5.8;

  // Defining and initializing a string variable
  String name = 'Eustace Ngure';

  // Defining and initializing a boolean variable
  bool isStudent = false;

  // Defining and initializing a list variable
